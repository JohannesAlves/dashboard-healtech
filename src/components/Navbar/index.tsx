                <Button
                  onClick={() => {
                    handleCloseNavMenu();
                    setActiveTab(page);
                  }}
                  sx={{
                    my: 2,
                    color: activeTab === page ? '#9747FF' : '#6A6A6A',
                    textTransform: 'none',
                    fontSize: 14,
                  }}
                >
                  {page}
                </Button>

                {activeTab === page && (
                  <Divider
                    color="#9747FF"
                    variant="inset"
                    sx={{
                      width: '100%',
                      height: 2,
                      position: 'absolute',
                      right: 0,
                    }}
                  />
                )}
